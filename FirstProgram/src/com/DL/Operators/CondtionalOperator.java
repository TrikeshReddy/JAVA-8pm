package com.DL.Operators;

public class CondtionalOperator {

	public static void main(String[] args) {
		
		int i=10,j=20;
		System.out.println(i==j);//false 
		//10.00==20 ==>10.00 == 20.00 ==>false
		System.out.println(i>=j);//flase
		System.out.println(i<=j);//true
		System.out.println(i>j);//false
		System.out.println(i<j);//true
		System.out.println(i!=j);//true
	}

}
