package com.DL.Operators;

public class AssignmentOperator {
	
public static void main(String[] args) {
	int j;//default
	
	j=10;//10
	System.out.println(j);
	j+=10;//20
	System.out.println(j);
	j*=10;//j=j*10;200
	System.out.println(j);
	j/=2;//100
	System.out.println(j);
	j%=9;//1
	System.out.println(j);
}
}
