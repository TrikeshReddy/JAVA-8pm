package com.DL.Operators;

public class ArthemeticOperators {
	//+,-,/,*,%
	public static void main(String[] args) {
		float i= 23.f;
		int j=10;
		float k;
		k=i+j;
		System.out.println("sum:"+k);
		k=i-j;
		System.out.println("diff:"+k);
		k=i/j;
		System.out.println("division:"+k);
		k=i*j;
		System.out.println("product:"+k);
		k=i%10;
		System.out.println("modulo division:"+k);
	}

}
