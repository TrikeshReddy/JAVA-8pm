package com.controls;

public class IfElse {
	public static void main(String[] args) {
		int i = 10;
		//simple if
		if(i>18) 
		{
			System.out.println("less than 18");
		}
		//if else
		if(i>18) {
			System.out.println("if got executed");
		}
		else {
			System.out.println("else got executed");
		}
	}
}
